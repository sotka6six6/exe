def get_vocab_for_prompt() -> str:
    """Возвращает случайную выборку из словаря для инъекции в промпт."""
    sample_mat    = random.sample(MAT,   min(80, len(MAT)))
    sample_slang  = random.sample(SLANG, min(30, len(SLANG)))
    sample_sarc   = random.sample(SARCASM, min(10, len(SARCASM)))
    sample_prov   = random.sample(PROVOCATIONS, min(6, len(PROVOCATIONS)))
    sample_combos = random.sample(COMBOS, min(5, len(COMBOS)))

    return (
        f"МАТ (используй свободно, чередуй — не повторяй одно слово дважды):\n{', '.join(sample_mat)}\n\n"
        f"СЛЕНГ И ОСКОРБЛЕНИЯ:\n{', '.join(sample_slang)}\n\n"
        f"САРКАЗМ (вплетай в ответ):\n{', '.join(sample_sarc)}\n\n"
        f"ПРОВОКАЦИИ:\n{', '.join(sample_prov)}\n\n"
        f"ГОТОВЫЕ КОМБО-ФРАЗЫ:\n{', '.join(sample_combos)}\n"
    )

def get_random_phrase(category: str) -> str:
    """Возвращает случайную фразу из категории."""
    phrases = SITUATIONAL.get(category, [])
    return random.choice(phrases) if phrases else ""

def get_random_mat(n: int = 3) -> list:
    """Возвращает n случайных матных слов."""
    return random.sample(MAT, min(n, len(MAT)))

def get_random_slang(n: int = 3) -> list:
    """Возвращает n случайных слов из сленга."""
    return random.sample(SLANG, min(n, len(SLANG)))

def get_random_combo() -> str:
    """Возвращает случайное комбо-оскорбление."""
    return random.choice(COMBOS)

def get_random_observation() -> str:
    """Возвращает случайное наблюдение."""
    return random.choice(OBSERVATIONS)

def get_situational(category: str) -> str:
    """Алиас для get_random_phrase."""
    return get_random_phrase(category)

