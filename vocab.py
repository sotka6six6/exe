def get_attitude_words(tone: str, count: int = 5) -> list[str]:
    """
    Возвращает список слов/фраз соответствующих тону отношения.
    Используется в build_system для обогащения инструкций.
    """
    bank = {
        "дружелюбный":  FRIENDLY_WORDS,
        "нейтральный":  NEUTRAL_WORDS,
        "настороженный": WARY_WORDS,
        "враждебный":   HOSTILE_WORDS,
        "ненависть":    HATE_WORDS,
    }.get(tone, NEUTRAL_WORDS)
    return random.sample(bank, min(count, len(bank)))


def detect_attitude_signal(text: str) -> tuple[float, float]:
    """
    Быстрый rule-based детект позитива/негатива без LLM.
    Возвращает (positive_score, negative_score) каждый 0..1.
    """
    t = text.lower()
    words = set(t.split())

    pos = sum(1 for w in POSITIVE_SIGNALS if w in t) / max(1, len(POSITIVE_SIGNALS) * 0.1)
    neg = sum(1 for w in NEGATIVE_SIGNALS if w in t) / max(1, len(NEGATIVE_SIGNALS) * 0.1)

    return min(1.0, pos), min(1.0, neg)




