def _gen_combos():
    """Генерирует комбинаторные словоформы программно."""
    prefixes_verb = [
        "за", "на", "по", "от", "пере", "про", "вы", "вз", "с", "у",
        "до", "при", "под", "об", "из", "раз", "съ", "в",
    ]
    suffixes_verb = ["ать", "ить", "еть", "нуть", "аться", "иться", "еться"]
    roots_mat = ["хуяр", "хуяч", "пиздяч", "пиздош", "ёб", "долбо", "муд"]
    
    prefixes_adj = ["за", "на", "от", "пере", "про", "раз", "вы", "об", "с"]
    suffixes_adj = ["анный", "ённый", "итый", "нутый", "аный", "еный"]
    
    results = []
    
    # Глаголы
    for p in prefixes_verb:
        for r in roots_mat:
            for s in suffixes_verb:
                w = f"{p}{r}{s}"
                if len(w) > 5:
                    results.append(w)
    
    # Прилагательные
    for p in prefixes_adj:
        for r in roots_mat:
            for s in suffixes_adj:
                w = f"{p}{r}{s}"
                if len(w) > 7:
                    results.append(w)
    
    return list(set(results))

